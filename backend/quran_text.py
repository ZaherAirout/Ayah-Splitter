def _load_text():
    """Load Quran text from JSON file or use built-in fallback."""
    global _TEXT_CACHE, _LOADED

    if _LOADED:
        return

    # Try loading from quran_text.json
    json_path = os.path.join(os.path.dirname(__file__), "quran_text.json")
    if os.path.exists(json_path):
        with open(json_path, "r", encoding="utf-8") as f:
            data = json.load(f)
            # Expected format: {"1": {"1": "text", "2": "text"}, ...}
            for surah_str, ayahs in data.items():
                surah_num = int(surah_str)
                _TEXT_CACHE[surah_num] = {
                    int(ayah_str): text for ayah_str, text in ayahs.items()
                }
    else:
        _TEXT_CACHE.update(_BUILTIN_TEXT)

    _LOADED = True


def get_ayah_text(surah: int, ayah: int) -> str:
    """Get Arabic text for a specific ayah. Returns empty string if unavailable."""
    _load_text()
    return _TEXT_CACHE.get(surah, {}).get(ayah, "")


def get_surah_text(surah: int) -> dict[int, str]:
    """Get all ayah texts for a surah. Returns empty dict if unavailable."""
    _load_text()
    return _TEXT_CACHE.get(surah, {})


def has_text(surah: int) -> bool:
    """Check if text is available for a surah."""
    _load_text()
    return surah in _TEXT_CACHE
